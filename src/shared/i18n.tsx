export type Texts = typeof zh;
const dicts: Record<Lang, Texts> = { zh, en };

const LANG_KEY = 'farely.lang';

interface I18n {
  lang: Lang;
  t: Texts;
  setLang: (lang: Lang) => void;
}

const I18nContext = createContext<I18n>({ lang: 'zh', t: zh, setLang: () => {} });

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLang] = useState<Lang>(() => {
    const saved = localStorage.getItem(LANG_KEY);
    if (saved === 'zh' || saved === 'en') return saved;
    return navigator.language.startsWith('zh') ? 'zh' : 'en';
  });

  useEffect(() => {
    localStorage.setItem(LANG_KEY, lang);
    document.documentElement.lang = lang === 'zh' ? 'zh-CN' : 'en';
  }, [lang]);

  return <I18nContext.Provider value={{ lang, t: dicts[lang], setLang }}>{children}</I18nContext.Provider>;
}

export function useI18n(): I18n {
  return useContext(I18nContext);
}

/** 按当前语言格式化账目分组的日期标题 */
export function formatDay(timestamp: number, lang: Lang): string {
  return new Date(timestamp).toLocaleDateString(lang === 'zh' ? 'zh-CN' : 'en-US', {
    month: 'short',
    day: 'numeric',
    weekday: 'short',
  });
}
